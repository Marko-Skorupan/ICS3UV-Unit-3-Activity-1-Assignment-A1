//Set the constants
const PI: number = 3.14159;
const radius: number = 5.6;

//Input none

//Calculate the area
let area: number = PI * radius * radius;

//OUTPUT
//display the result
console.log("The area of a circle with a radius of " + PI, "cm is " + area, "cm squared")

